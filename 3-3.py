# Original array
numbers = [8, 2, 5, 1, 9]

# Print the original array
print("Array:", end=" ")
for num in numbers:
    print(num, end=" ")
print()  

# Calculate the second power (square) of each element
print("2nd power:", end=" ")
for num in numbers:
    print(num ** 2, end=" ")
print()  
